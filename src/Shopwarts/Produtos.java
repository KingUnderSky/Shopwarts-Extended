package Shopwarts;

public class Produtos {
    
    //Atributos
    
    private int preco;
    private String id;
    private String pacote;
    
    
    // getter setter

    public int getPreco() {
        return preco;
    }

    public void setPreco(int preco) {
        this.preco = preco;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPacote() {
        return pacote;
    }

    public void setPacote(String pacote) {
        this.pacote = pacote;
    }
    
   
    
    
}
